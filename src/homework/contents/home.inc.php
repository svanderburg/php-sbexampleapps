<p>
This is an application that can be used to help you improving your skills for
written exams, by providing the following features:
</p>

<ul>
	<li>You can create <strong>tests</strong> containing a set of questions.</li>
	<li>
	Questions can refer to <strong>answers</strong> that should match exactly or answers that
	should match loosely. The system checks whether exact answers match the provided
	answers. The latter is particularly useful for preparing vocabulary tests.
	</li>
	<li>
	You can take example <strong>exams</strong> to investigate how well you have
	mastered your required knowledge.
	</li>
</ul>
