<p>
This system can be used to manage the literature provided as part of academic conferences. More specifically you can:
</p>

<ul>
<li>Manage <strong>authors</strong>.</li>
<li>Manage <strong>publishers</strong>.</li>
<li>Manage <strong>conferences</strong> and the <strong>papers</strong> part of their proceedings.</li>
<li>Export <strong>BibTeX</strong> citations.</li>
<li>A fuzzy <strong>search</strong> function for papers.</li>
</ul>
