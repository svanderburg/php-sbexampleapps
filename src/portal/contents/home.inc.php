<p>
The main purpose of this application is to be a portal that redirects the user to other subsystems.
</p>

<p>
In addition to redirection, this portal application also provides the following embedded features:
</p>

<ul>
<li>A fully manageable <strong>news</strong> section including an RSS feed.</li>
<li>A dynamically editable <strong>changelog</strong> page.</li>
<li>A page displaying various HTML elements to test the <strong>layout</strong>.</li>
<li>A <strong>gallery</strong> sub application that can be managed by end-users.</li>
</ul>
